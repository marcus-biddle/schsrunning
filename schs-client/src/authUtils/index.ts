import Cookies from 'js-cookie';

// Function to set the access token cookie
export function setAccessTokenCookie(accessToken: string, expiration: number) {
  Cookies.set('accessToken', accessToken, { expires: expiration });
}

// Function to get the access token cookie value
export function getAccessTokenCookie() {
  return Cookies.get('accessToken');
}

// Function to check if the access token cookie is set
export function isAccessTokenCookieSet() {
  return Cookies.get('accessToken') !== undefined;
}

export const deleteAccessTokenCookie = () => {
  Cookies.remove('accessToken');
};
  